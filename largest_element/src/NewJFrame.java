    public void fillArray(int[] a) {
        for (int i = 0; i < a.length; i++) {
            a[i] = (int) (Math.random() * 11);
        }

    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Algo_task4 task1;
        XYSeries xy = new XYSeries("sort");
        for (int i = 1; i <= 400; i *= 5) {
            int a[] = new int[i * 200];
            fillArray(a);
            //time
            double T1 = System.currentTimeMillis();
            task1 = new Algo_task4(a.length - 1, a);
            task1.largest(0, a.length - 1);
            double T2 = System.currentTimeMillis();
            double T = T2 - T1;
            xy.add(a.length, T);

        }
        XYSeriesCollection dataSet = new XYSeriesCollection();
        dataSet.addSeries(xy);
        JFreeChart chart = ChartFactory.createXYLineChart("largest element of an array", "Array size", "time", dataSet, PlotOrientation.VERTICAL, true, true, false);
        ///////////////
        ChartPanel cp = new ChartPanel(chart);
        cp.setPreferredSize(new java.awt.Dimension(500, 270));
        JFrame plot = new JFrame("plot");
        plot.setContentPane(cp);
        plot.setSize(500, 500);
        plot.setVisible(true);

    }//GEN-LAST:event_jButton2ActionPerformed

