     public static int[] StringToIntArray(String input) {
        String[] stringArray = input.split("\\s+");
        int[] arr = new int[stringArray.length];

        for (int i = 0; i < stringArray.length; i++) {
            arr[i] = Integer.parseInt(stringArray[i]);
        }

        return arr;
    }


