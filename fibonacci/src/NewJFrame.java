    public static int recursiveFib(int n) {
        if (n <= 1) {
            return n;
        } else {
            return recursiveFib(n - 1) + recursiveFib(n - 2);
        }

    }

    public static int iterativeFib(int n) {
        int f[] = new int[n + 1];
        f[0] = 0;
        if (n > 0) {
            f[1] = 1;
            for (int i = 2; i < f.length; i++) {
                f[i] = f[i - 1] + f[i - 2];
            }
        }
        return f[n];
    }
