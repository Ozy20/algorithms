    public void fillArray(int[] a) {
        for (int i = 0; i < a.length; i++) {
            a[i] = (int) (Math.random() * 11);
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      XYSeries xy1 = new XYSeries("sort");
        for (int i = 0; i <=200; i++) {
            int a[] = new int[i * 200];
            fillArray(a);
            //time
            double T1 = System.currentTimeMillis();
            for(int j=0;j<a.length;j++){
            iterativeFib(a[j]);
            }
            double T2 = System.currentTimeMillis();
            double T = T2 - T1;
            xy1.add(a.length, T);

        }
        ////////////////
        XYSeriesCollection dataSet1 = new XYSeriesCollection();
        dataSet1.addSeries(xy1);
        
        JFreeChart chart1 = ChartFactory.createXYLineChart("iterative fib", "Array size", "time", dataSet1, PlotOrientation.VERTICAL, true, true, false);
        ///////////////
        ChartPanel cp1 = new ChartPanel(chart1);
        cp1.setPreferredSize(new java.awt.Dimension(500,270));
        JFrame plot = new JFrame("plot");
        plot.setContentPane(cp1);
        plot.setSize(500,500);
        plot.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

