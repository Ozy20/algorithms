    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
         int n = Integer.parseInt(jTextField3.getText());
    String[][] ws = new String[n][n];

    try {
        JFileChooser jFileChooser1 = new JFileChooser();
        int choice = jFileChooser1.showOpenDialog(this);
        if (choice == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser1.getSelectedFile();
            Scanner scan = new Scanner(file);

            for (int i = 0; i < n; i++) {
                for (int j = 0; j < n; j++) {
                    ws[i][j] = scan.next();
                    if (ws[i][j].equals("inf")) {
                        ws[i][j] = "999999"; // Use a large value as infinity
                    }
                }
            }
        }

        int[][] matrixW = new int[n][n];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                matrixW[i][j] = Integer.parseInt(ws[i][j]);
            }
        }

        f = new FloydS(n, matrixW);

        String s = "";
        for (int i = 0; i < n; i++) {
            s += Arrays.toString(matrixW[i]) + "\n";
        }

        jTextArea3.setText(s);

    } catch (Exception e) {
        e.printStackTrace(); // Handle the exception appropriately
    }

    }//GEN-LAST:event_jButton7ActionPerformed

