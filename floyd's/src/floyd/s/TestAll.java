    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // ... Existing code

    XYSeries series = new XYSeries("Time Complexity");

    for (int i = 1; i <= 30; i++) {
        int size = i * 10;
        int[][] a = new int[size][size];
        fillList2D(a, size);

        long start = System.currentTimeMillis();
        // Assuming f is already initialized and contains Floyd algorithm
        f.floyd();
        long end = System.currentTimeMillis();
        series.add(size, end - start);
    }

    XYSeriesCollection dataset = new XYSeriesCollection();
    dataset.addSeries(series);

    JFreeChart chart = ChartFactory.createXYLineChart(
            "Floyd Time Complexity",
            "Size",
            "Time (ms)",
            dataset,
            PlotOrientation.VERTICAL,
            true,
            true,
            false
    );

    ChartPanel chartPanel = new ChartPanel(chart);
    chartPanel.setPreferredSize(new java.awt.Dimension(500, 270));

    JFrame plotFrame = new JFrame("Time Complexity Plot");
    plotFrame.setContentPane(chartPanel);
    plotFrame.setSize(500, 500);
    plotFrame.setVisible(true);

    }//GEN-LAST:event_jButton10ActionPerformed

